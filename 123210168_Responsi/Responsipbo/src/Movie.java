        String alur = jTextField2.getText();
        String penokohan = jTextField3.getText();
        String akting = jTextField4.getText();
       
        double nilai = 0;
        double nilaialur, nilaitokoh, nilaiakting;
        nilaialur = Double.valueOf(alur);
        nilaitokoh = Double.valueOf(penokohan);
        nilaiakting = Double.valueOf(akting);
        nilai = (nilaialur+nilaitokoh+nilaiakting)/3;
        
        try{
            String sql = "INSERT INTO movie VALUES(?,?,?,?,?)";
            conn = new konektor();
            koneksi = conn.getKoneksi();
            pst = koneksi.prepareStatement(sql);
            
            pst.setString(1, judul);
            pst.setDouble(2, nilaialur);
            pst.setDouble(3, nilaitokoh);
            pst.setDouble(4, nilaiakting);
            pst.setDouble(5, nilai);
            pst.executeUpdate();
            pst.close();
            
        }catch (Exception e){
            System.out.println(e);
        }
        tampilData();                     
