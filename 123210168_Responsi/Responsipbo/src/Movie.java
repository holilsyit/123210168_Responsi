    public void tampilData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            String sql = "SELECT * FROM movie";
            conn = new konektor();
            koneksi = conn.getKoneksi();
            statement = koneksi.createStatement();
            rs = statement.executeQuery(sql);

            while (rs.next()) {
                Object[] o = new Object[5];
                o[0] = rs.getString("judul");
                o[1] = rs.getString("alur");
                o[2] = rs.getString("penokohan");
                o[3] = rs.getString("akting");
                o[4] = rs.getString("nilai");

                model.addRow(o);
            }
            rs.close();
            statement.close();
        } catch (Exception e) {
            System.out.println("Terjadi kesalahan pada tampil data");
        }
    }

