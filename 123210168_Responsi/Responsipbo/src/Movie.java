    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int i = jTable1.getSelectedRow();
        if(i == -1) {
            return;
        }
        String judul = jTextField1.getText();
        String alur = jTextField2.getText();
        String penokohan = jTextField3.getText();
        String akting = jTextField4.getText();
        
        double nilai = 0;
        double nilaialur, nilaitokoh, nilaiakting;
        nilaialur = Double.valueOf(alur);
        nilaitokoh = Double.valueOf(penokohan);
        nilaiakting = Double.valueOf(akting);
        nilai = (nilaialur+nilaitokoh+nilaiakting)/3;
        
        try{
            String sql = "UPDATE movie set alur=?, penokohan=?, akting=?, nilai=? WHERE judil=?";
            conn = new konektor();
            koneksi = conn.getKoneksi();
            pst = koneksi.prepareStatement(sql);
            
            pst.setString(1, alur);
            pst.setString(2, penokohan);
            pst.setString(3, akting);
            pst.setDouble(4, nilai);
            pst.setString(5, judul);
            
            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null,"Data Berhasil Diubah");
            
        } catch(Exception e){
            System.out.println("Update Error");
            System.out.println(e);
        }finally{
            tampilData();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

